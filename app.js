function start() {
  click(".js--bell", ".header__setting-noti");
  click(".header__user-avt", ".header__user-info");
  slider(".content__panel-logo", "js--panel-hover");
  slider(".header__directional-items", "js--nav-hover");
  slider(".subHeader__main-items", "js--nav-hover");
  contentChange();
  arrowSider();
  console.log("start");
  sliderShow();
}

function click(main, subMain) {
  var main = document.querySelector(`${main}`);
  var subMain = document.querySelector(`${subMain}`);
  main.addEventListener("click", function () {
    var x = subMain.classList.contains("show");
    if (x == true) {
      subMain.classList.remove("opct");
      subMain.classList.remove("show");
    }
    if (x == false) {
      subMain.classList.add("show");
      subMain.classList.add("opct");
    }
  });
}

function slider(iconALl, hover) {
  var icons = document.querySelectorAll(iconALl);
  icons.forEach(function (icon) {
    icon.addEventListener("click", function (e) {
      icons.forEach(function (icon) {
        if (icon.classList.contains(hover)) {
          icon.classList.remove(hover);
        }
      });
      icon.classList.add(hover);
    });
  });
}

function contentChange() {
  document.querySelector(".content__panel-heading").innerText =
    "Hi, I'm Dũng welcome to my website";
  let main = document.querySelector(".content__theme-main");
  var icons = document.querySelectorAll(".content__panel-items");
  var content = document.querySelector(".content__design-theme");
  var heading = content.querySelector(".content__theme-heading");
  var container = content.querySelectorAll(".content__theme-container");
  icons.forEach(function (icon, indexBtn) {
    icon.addEventListener("click", function () {
      main.style.left = "0";
      var spanHeading = icon.querySelector(".design--span").innerText;
      heading.innerHTML = spanHeading;
      var sub = content.querySelectorAll(".sub__theme-img");
      var imgSub = content.querySelectorAll(".content__theme-images");
      sub.forEach(function (subt, i) {
        subt.innerHTML = spanHeading;
      });
      imgSub.forEach(function (imgsubt, i) {
        imgsubt.setAttribute("src", img[indexBtn][i]);
      });
    });
  });
}

function arrowSider() {
  var logo = document.querySelectorAll(".content__panel-logo");
  var arrow = document.querySelector(".arrow");
  logo.forEach(function (log) {
    log.addEventListener("click", function () {
      var left = log.offsetLeft;
      var space = left + 12;
      arrow.style.left = `${space}px`;
    });
  });
}

function sliderShow() {
  let main = document.querySelector(".content__theme-main");
  let contentMain = document.querySelectorAll(".content__theme-container");
  let contentMain1 = document.querySelector(".content__theme-container");
  let width = contentMain.length * contentMain1.offsetWidth * 3.5;
  main.style.width = `${width}px`;
  let btn = document.querySelectorAll(".js--btn");
  function check() {
    if (main.offsetLeft !== 0) {
      btn[0].classList.add("act");
      btn[1].classList.add("act");
    }
    if (main.offsetLeft == 0) {
      btn[0].classList.remove("act");
      btn[1].classList.add("act");
    }
  }
  btn[0].addEventListener("click", function () {
    main.style.left = `${main.offsetLeft + 400}px`;
    check();
  });
  btn[1].addEventListener("click", function () {
    main.style.left = `${main.offsetLeft - 400}px`;
    check();
  });
  check();
}
